The Tech Directory is free, <a href="http://en.wikipedia.org/wiki/Crowdsourcing">crowd-sourced</a>, database of "technology-oriented" businesses, organizations, and individuals in Central Oregon.  This Who's Who of High Desert tech" is provided by the <a href="http://techallianceco.org">Tech Alliance of Central Oregon</a> to make it easier for you to connect with our local tech community.
</p>
